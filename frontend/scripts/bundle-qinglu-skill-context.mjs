/**
 * Bundle Agent/burnpal_skill into a TS module for system-prompt injection (Vercel / 百炼).
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const SKILL_ROOT = path.resolve(__dirname, '../../Agent/burnpal_skill')
const OUT_FILE = path.resolve(__dirname, '../src/generated/qingluSkillContext.ts')
const SOURCE_SHA = '3e504c6fd4d322164f3c788fb629e22db79c749e'

const INCLUDE_EXT = new Set(['.md', '.json'])

function walkFiles(dir, base = '') {
  const out = []
  if (!fs.existsSync(dir)) {
    console.error('Missing skill root:', SKILL_ROOT)
    process.exit(1)
  }
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    if (ent.name === '.git') continue
    const rel = path.join(base, ent.name).replace(/\\/g, '/')
    const full = path.join(dir, ent.name)
    if (ent.isDirectory()) out.push(...walkFiles(full, rel))
    else if (INCLUDE_EXT.has(path.extname(ent.name).toLowerCase())) out.push({ rel, full })
  }
  return out.sort((a, b) => a.rel.localeCompare(b.rel))
}

function readEntry({ rel, full }) {
  let body = fs.readFileSync(full, 'utf8')
  if (rel.endsWith('.json')) {
    try {
      body = JSON.stringify(JSON.parse(body))
    } catch {
      /* keep raw */
    }
  }
  return `\n\n=== burnpal_skill/${rel} ===\n${body.trim()}`
}

const files = walkFiles(SKILL_ROOT)
const context = [
  '# QingLu OpenClaw Skill Pack (full vendor copy)',
  `Source: CCLYX/burnpal.skill @ ${SOURCE_SHA}`,
  'Rules: Route per root SKILL.md; recommendations MUST use store/activity names from JSON assets only; never invent POIs.',
  ...files.map(readEntry),
].join('')

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true })
const ts = `// Auto-generated by scripts/bundle-qinglu-skill-context.mjs — do not edit manually
/** Full burnpal_skill pack injected into chat system prompt (production + dev). */
export const QINGLU_SKILL_BUNDLED_AT = ${JSON.stringify(new Date().toISOString())} as const
export const QINGLU_SKILL_SOURCE_SHA = ${JSON.stringify(SOURCE_SHA)} as const
export const QINGLU_SKILL_FILE_COUNT = ${files.length} as const

const QINGLU_SKILL_CONTEXT: string = ${JSON.stringify(context)}

export function getQingluSkillSystemContext(): string {
  return QINGLU_SKILL_CONTEXT
}

/** @deprecated use getQingluSkillSystemContext */
export const getBurnpalSkillSystemContext = getQingluSkillSystemContext
`

fs.writeFileSync(OUT_FILE, ts, 'utf8')
console.log(
  `bundled ${files.length} files → ${OUT_FILE} (${Math.round(context.length / 1024)} KiB text)`,
)
