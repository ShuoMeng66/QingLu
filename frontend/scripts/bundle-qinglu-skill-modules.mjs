/**
 * Bundle Agent/burnpal_skill into per-module chunks for dynamic system-prompt injection (Scheme B).
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const SKILL_ROOT = path.resolve(__dirname, '../../Agent/burnpal_skill')
const OUT_FILE = path.resolve(__dirname, '../src/generated/qingluSkillModules.ts')
const SOURCE_SHA = '3e504c6fd4d322164f3c788fb629e22db79c749e'

const INCLUDE_EXT = new Set(['.md', '.json'])

const SHARED_FILES = [
  'SKILL.md',
  'assets/user-profiles.json',
  'references/evolved-lessons.md',
]

const MODULE_DIRS = {
  'skill1-diet': 'skills/skill1-diet',
  'skill2-venue': 'skills/skill2-venue',
  'skill3-recovery': 'skills/skill3-recovery',
  'skill4-social': 'skills/skill4-social',
  general: '',
}

function walkFiles(dir, base = '') {
  const out = []
  if (!fs.existsSync(dir)) return out
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    if (ent.name === '.git') continue
    const rel = path.join(base, ent.name).replace(/\\/g, '/')
    const full = path.join(dir, ent.name)
    if (ent.isDirectory()) out.push(...walkFiles(full, rel))
    else if (INCLUDE_EXT.has(path.extname(ent.name).toLowerCase())) out.push({ rel, full })
  }
  return out.sort((a, b) => a.rel.localeCompare(b.rel))
}

function readEntry(rel, full) {
  let body = fs.readFileSync(full, 'utf8')
  if (rel.endsWith('.json')) {
    try {
      body = JSON.stringify(JSON.parse(body))
    } catch {
      /* keep raw */
    }
  }
  return `\n\n=== burnpal_skill/${rel} ===\n${body.trim()}`
}

function bundlePaths(paths) {
  return paths
    .map((rel) => {
      const full = path.join(SKILL_ROOT, rel)
      if (!fs.existsSync(full)) {
        console.warn('skip missing:', rel)
        return ''
      }
      return readEntry(rel, full)
    })
    .join('')
}

const sharedContext = [
  '# QingLu OpenClaw — Router + shared assets',
  `Source: CCLYX/burnpal.skill @ ${SOURCE_SHA}`,
  'Runtime: per-request module injection (router layer + one skill module).',
  bundlePaths(SHARED_FILES),
].join('')

const moduleContext = {}
for (const [id, subdir] of Object.entries(MODULE_DIRS)) {
  if (id === 'general') {
    moduleContext[id] =
      '\n\n=== general ===\nNo submodule SKILL loaded. Answer with router rules only; ask one clarifying question if intent is unclear.'
    continue
  }
  const dir = path.join(SKILL_ROOT, subdir)
  const files = walkFiles(dir, subdir)
  moduleContext[id] = files.map(({ rel, full }) => readEntry(rel, full)).join('')
}

const moduleKeys = Object.keys(MODULE_DIRS)
const ts = `// Auto-generated by scripts/bundle-qinglu-skill-modules.mjs — do not edit manually

export type QingluSkillModuleId =
${moduleKeys.map((k) => `  | '${k}'`).join('\n')}

export const QINGLU_SKILL_MODULE_IDS: QingluSkillModuleId[] = ${JSON.stringify(moduleKeys)}

export const QINGLU_SKILL_BUNDLED_AT = ${JSON.stringify(new Date().toISOString())} as const
export const QINGLU_SKILL_SOURCE_SHA = ${JSON.stringify(SOURCE_SHA)} as const

/** Router SKILL.md + user profiles + evolved lessons */
export const QINGLU_SKILL_SHARED_CONTEXT: string = ${JSON.stringify(sharedContext)}

export const QINGLU_SKILL_MODULE_CONTEXT: Record<QingluSkillModuleId, string> = ${JSON.stringify(moduleContext, null, 2)} as Record<QingluSkillModuleId, string>

export function getQingluSkillModuleContext(moduleId: QingluSkillModuleId): string {
  return QINGLU_SKILL_SHARED_CONTEXT + '\\n\\n--- Active Skill module: ' + moduleId + ' ---' + (QINGLU_SKILL_MODULE_CONTEXT[moduleId] ?? '')
}

/** @deprecated Scheme A full pack — use getQingluSkillModuleContext + routeQingluSkillModule instead */
export function getQingluSkillSystemContext(): string {
  return QINGLU_SKILL_MODULE_IDS.map((id) => getQingluSkillModuleContext(id)).join('')
}
`

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true })
fs.writeFileSync(OUT_FILE, ts, 'utf8')

const sharedKiB = Math.round(sharedContext.length / 1024)
const moduleKiB = Object.fromEntries(
  moduleKeys.map((id) => [id, Math.round((moduleContext[id]?.length ?? 0) / 1024)]),
)
console.log('shared', `${sharedKiB} KiB`, 'modules', moduleKiB, '→', OUT_FILE)
