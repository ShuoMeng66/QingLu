/**
 * Bundle demo user profiles for onboarding flow.
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const SRC = path.resolve(__dirname, '../../Agent/burnpal_skill/assets/user-profiles.json')
const OUT = path.resolve(__dirname, '../src/data/demoProfiles.generated.ts')

const profiles = JSON.parse(fs.readFileSync(SRC, 'utf8'))
const ts = `// Auto-generated by scripts/bundle-qinglu-demo-profiles.mjs
export interface DemoProfileTodayMeal {
  meal: string
  items: string
  estimated_kcal: number
}

export interface DemoProfileToday {
  training_plan: string
  meals_logged: DemoProfileTodayMeal[]
  total_intake_kcal: number
  remaining_kcal: number
}

export interface DemoProfileLocation {
  city: string
  work: string
  home: string
  current: string
}

export interface DemoProfileRecord {
  id: string
  name: string
  gender: 'male' | 'female'
  age: number
  height_cm: number
  weight_kg: number
  target_weight_kg: number
  bmr_kcal: number
  tdee_kcal: number
  daily_target_kcal: number
  activity_level: string
  training_type: string
  training_frequency: string
  dietary_restrictions: string[]
  taste_preferences: string[]
  budget_per_meal_yuan: number
  budget_dining_per_person_yuan: number
  location: DemoProfileLocation
  today: DemoProfileToday
  sport_interests: string[]
  preferred_scale: string
  preferred_time: string
}

export const DEMO_PROFILES: DemoProfileRecord[] = ${JSON.stringify(profiles, null, 2)}
`

fs.mkdirSync(path.dirname(OUT), { recursive: true })
fs.writeFileSync(OUT, ts, 'utf8')
console.log(`bundled ${profiles.length} demo profiles → ${OUT}`)
