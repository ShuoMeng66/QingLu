/**
 * Flatten burnpal_skill venue JSON for UI cards (map + images).
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const SKILL = path.resolve(__dirname, '../../Agent/burnpal_skill')
const OUT = path.resolve(__dirname, '../src/data/qingluVenues.generated.ts')

function readJson(rel) {
  return JSON.parse(fs.readFileSync(path.join(SKILL, rel), 'utf8'))
}

function coords(row) {
  const c = row.coordinates
  if (!c) return {}
  const lat = c.latitude ?? c.lat
  const lon = c.longitude ?? c.lon
  if (lat == null || lon == null) return {}
  return { lat: Number(lat), lon: Number(lon) }
}

function pushVenues(list, type, rows, extra = () => ({})) {
  for (const row of rows) {
    list.push({
      id: row.id,
      type,
      name: row.name,
      area: row.area ?? row.district ?? row.address?.split('·')[0] ?? '',
      address: row.address ?? row.area ?? '',
      cuisine: row.cuisine ?? row.type ?? row.sport_type ?? row.sport ?? '',
      rating: row.rating ?? null,
      avgPrice: row.avg_price_per_person ?? row.avg_price_yuan ?? null,
      dietScore: row.diet_friendly_score ?? null,
      listingUrl: row.meituan_url ?? row.dianping_url ?? row.listing_url ?? undefined,
      ...coords(row),
      ...extra(row),
    })
  }
}

const venues = []
pushVenues(venues, 'food', readJson('skills/skill1-diet/assets/restaurants.json'))
pushVenues(venues, 'takeout', readJson('skills/skill1-diet/assets/takeout.json'))
pushVenues(venues, 'gym', readJson('skills/skill2-venue/assets/gyms.json'), (r) => ({
  tags: (r.equipment ?? r.features ?? []).slice(0, 2),
}))
pushVenues(venues, 'gym', readJson('skills/skill2-venue/assets/sports-venues.json'), (r) => ({
  cuisine: r.venue_type ?? '运动场馆',
}))
pushVenues(venues, 'recovery', readJson('skills/skill3-recovery/assets/recovery-shops.json'), (r) => ({
  cuisine: r.type ?? '恢复',
}))
for (const row of readJson('skills/skill4-social/assets/activities.json')) {
  venues.push({
    id: row.id,
    type: 'activity',
    name: row.venue_name ?? row.title,
    area: row.district ?? row.city ?? '',
    address: row.venue_address ?? row.venue_name ?? '',
    cuisine: row.type ?? '活动',
    rating: null,
    avgPrice: null,
    dietScore: null,
    listingUrl: row.meituan_url ?? row.dianping_url ?? undefined,
    tags: row.tags?.slice(0, 2) ?? [],
    aliases: row.title ? [row.title] : [],
  })
}

const ts = `// Auto-generated by scripts/bundle-qinglu-venues.mjs
export type QingluVenueType = 'food' | 'takeout' | 'gym' | 'recovery' | 'activity'

export interface QingluVenueRecord {
  id: string
  type: QingluVenueType
  name: string
  area: string
  address: string
  cuisine: string
  rating: number | null
  avgPrice: number | null
  dietScore: number | null
  lat?: number
  lon?: number
  tags?: string[]
  aliases?: string[]
  listingUrl?: string
}

export const QINGLU_VENUES: QingluVenueRecord[] = ${JSON.stringify(venues, null, 2)}
`

fs.mkdirSync(path.dirname(OUT), { recursive: true })
fs.writeFileSync(OUT, ts, 'utf8')
console.log(`bundled ${venues.length} venues → ${OUT}`)
