/**
 * Bundle takeout.json for frontend card resolution (combos, item_id lookup).
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const SRC = path.resolve(
  __dirname,
  '../../Agent/burnpal_skill/skills/skill1-diet/assets/takeout.json',
)
const OUT = path.resolve(__dirname, '../src/data/qingluTakeout.generated.ts')

const rows = JSON.parse(fs.readFileSync(SRC, 'utf8'))

const catalog = rows.map((row) => ({
  id: row.id,
  name: row.name,
  area: row.area ?? '',
  rating: row.rating ?? null,
  avg_price_yuan: row.avg_price_yuan ?? null,
  delivery_time_min: row.delivery_time_min ?? null,
  combos: (row.combos ?? []).map((c) => ({
    name: c.name,
    kcal_range: c.kcal_range ?? '',
    protein_g: c.protein_g ?? null,
    price_yuan: c.price_yuan ?? null,
    warnings: c.warnings ?? [],
    tags: c.tags ?? [],
  })),
}))

const ts = `// Auto-generated by scripts/bundle-qinglu-takeout.mjs
export interface QingluTakeoutCombo {
  name: string
  kcal_range: string
  protein_g: number | null
  price_yuan: number | null
  warnings: string[]
  tags: string[]
}

export interface QingluTakeoutRecord {
  id: string
  name: string
  area: string
  rating: number | null
  avg_price_yuan: number | null
  delivery_time_min: number | null
  combos: QingluTakeoutCombo[]
}

export const QINGLU_TAKEOUT: QingluTakeoutRecord[] = ${JSON.stringify(catalog, null, 2)}
`

fs.mkdirSync(path.dirname(OUT), { recursive: true })
fs.writeFileSync(OUT, ts, 'utf8')
console.log(`bundled ${catalog.length} takeout venues → ${OUT}`)
